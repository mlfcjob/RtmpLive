#pragma once


#include <QMutex>
#include "XData.h"
#include <list>
#include <QThread>

class XDataThread:public QThread
{
public:
	// （缓冲列表）列表最大值，超出删除最旧的数据（最前面）
	int  maxList = 100;

	//在列表结尾插入
	virtual void Push(XData d);
	//读取列表中最早的数据, 返回数据需要调用XData.Drop()清理
	virtual XData Pop();

	//启动线程
	virtual bool Start();
	//退出线程并等待线程退出（阻塞函数）
	virtual void Stop();

	virtual void Clear();

	XDataThread();
	~XDataThread();

protected:
	//存放交互数据 插入策略 先进先出
	std::list<XData> datas;

	//交互数据列表大小
	int dataCount = 0;
	//互斥访问 datas
	QMutex mutex;

	//处理线程退出
	bool isExit = false;
};

